// Get daily challenge based on date
export function getDailyChallenge(): string {
  const today = new Date().toDateString();
  const index = hashCode(today) % DAILY_CHALLENGES.length;
  return DAILY_CHALLENGES[Math.abs(index)];
}

// Simple hash function for deterministic daily challenges
function hashCode(str: string): number {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    const char = str.charCodeAt(i);
    hash = (hash << 5) - hash + char;
    hash = hash & hash; // Convert to 32-bit integer
  }
  return hash;
}
